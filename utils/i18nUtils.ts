/**
 * Get RTL status for a language
 */
export function isRTL(language: string): boolean {
  const rtlLanguages = ['ar', 'he', 'fa', 'ur', 'dv', 'ha', 'ku', 'ps', 'sd', 'ug', 'yi'];
  return rtlLanguages.includes(language.toLowerCase());
}