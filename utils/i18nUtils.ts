/**
 * Format date according to locale
 */
export function formatDate(date: Date | string, locale: string = getCurrentLanguage(), options: Intl.DateTimeFormatOptions = {}): string {
  const dateObj = typeof date === 'string' ? new Date(date) : date;
  return new Intl.DateTimeFormat(locale, {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
    ...options
  }).format(dateObj);
}

/**
 * Format time according to locale
 */
export function formatTime(date: Date | string, locale: string = getCurrentLanguage(), options: Intl.DateTimeFormatOptions = {}): string {
  const dateObj = typeof date === 'string' ? new Date(date) : date;
  return new Intl.DateTimeFormat(locale, {
    hour: '2-digit',
    minute: '2-digit',
    ...options
  }).format(dateObj);
}

