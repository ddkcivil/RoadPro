/**
 * Get translation for a given key
 * @param key - The translation key (dot notation supported for nested keys)
 * @param language - The language code (defaults to stored language or English)
 * @param params - Optional parameters for string interpolation
 * @returns Translated string or the key if translation is not found
 */
export function getTranslation(key: string, language: string = DEFAULT_LANGUAGE, params?: Record<string, any>): string {
  // Split the key to handle nested translations
  const keys = key.split('.');
  let translation: any = ENGLISH_TRANSLATIONS; // Default to English if language not found

  // Navigate through nested translation objects
  for (const k of keys) {
    if (translation && typeof translation === 'object' && translation[k]) {
      translation = translation[k];
    } else {
      // Return the original key if translation not found
      return key;
    }
  }

  // If we found a translation but it's an object, return the key
  if (typeof translation === 'object') {
    return key;
  }

  let result = String(translation);

  // Perform parameter substitution if params are provided
  if (params) {
    Object.entries(params).forEach(([paramKey, value]) => {
      result = result.replace(new RegExp(`{{\s*${paramKey}\s*}}`, 'g'), String(value));
    });
  }

  return result;
}

/**
 * Get current language from localStorage
 */
export function getCurrentLanguage(): string {
  if (typeof window !== 'undefined') {
    return localStorage.getItem(LANGUAGE_STORAGE_KEY) || DEFAULT_LANGUAGE;
  }
  return DEFAULT_LANGUAGE;
}

/**
 * Set current language in localStorage
 */
export function setCurrentLanguage(language: string): void {
  if (typeof window !== 'undefined') {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
  }
}

